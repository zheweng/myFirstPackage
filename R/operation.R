#' Sum of two numeric values
#'
#' @param x A numeric value
#' @param y A numeric value
#
#' @return the sum of these two numeric values
#' @export
#'
#' @examples
#' sum(1,2)
#' sum(3,5)
mySum <- function(x,y){
  return (x+y)
}

#' Difference of two numeric values
#'
#' @param x A numeric value
#' @param y A numeric value
#
#' @return the difference of these two numeric values
#' @export
#'
#' @examples
#' difference(2,1)
#' difference(1,2)
difference <- function(x,y){
  return(x-y)
}

#' Product of two numeric values
#'
#' @param x A numeric value
#' @param y A numeric value
#
#' @return the product of these two numeric values
#' @export
#'
#' @examples
#' product(1,2)
#' product(2,3)
product <- function(x,y){
  return(x*y)
}

#' Quotient of two numeric values
#'
#' @param x A numeric value
#' @param y A numeric value
#
#' @return the quotient of these two numeric values
#' @export
#'
#' @examples
#' quorient(1,2)
#' quotient(2,3)
#' quotient(3,3)
quorient <- function(x,y){
  return(x/y)
}



